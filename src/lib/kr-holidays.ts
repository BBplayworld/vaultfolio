export function isKrHoliday(dateStr: string): boolean {
  return KR_HOLIDAYS.has(dateStr);
}

export function isKrBusinessDay(d: Date): boolean {
  const day = d.getUTCDay();
  if (day === 0 || day === 6) return false;
  const yyyy = d.getUTCFullYear();
  const mm = String(d.getUTCMonth() + 1).padStart(2, "0");
  const dd = String(d.getUTCDate()).padStart(2, "0");
  return !isKrHoliday(`${yyyy}-${mm}-${dd}`);
}

// 영업일 아니면 직전 영업일까지 -1일씩 (최대 10일)
export function rollbackToBusinessDay(d: Date): Date {
  const r = new Date(d);
  for (let i = 0; i < 10; i++) {
    if (isKrBusinessDay(r)) return r;
    r.setUTCDate(r.getUTCDate() - 1);
  }
  return r;
}

// 영업일 아니면 다음 영업일까지 +1일씩 (월초 첫 영업일 찾기용)
export function forwardToBusinessDay(d: Date): Date {
  const r = new Date(d);
  for (let i = 0; i < 10; i++) {
    if (isKrBusinessDay(r)) return r;
    r.setUTCDate(r.getUTCDate() + 1);
  }
  return r;
}
